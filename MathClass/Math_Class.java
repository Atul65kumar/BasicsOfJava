package MathClass;

public class Math_Class {

	public static void main(String[] args) 
	{
		System.out.println(Math.addExact(45, 85)); //int
		System.out.println(Math.addExact(453456l, 56)); //long
		System.out.println(Math.subtractExact(45, 56));
		System.out.println(Math.multiplyExact(5, 6));
		System.out.println(Math.max(453456l, 7777777));
		System.out.println(Math.min(453456l, 56876));
		System.out.println(Math.sqrt(81));
		System.out.println(Math.random());
		System.out.println(Math.abs(-90));
		System.out.println(Math.PI);
	}

}
